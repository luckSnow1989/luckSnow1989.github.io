二、归档
   1.查看当前的日志模式。
       使用dba登录：sqlplus  /as sysdba
       查看模式：archive log list
   2.默认数据库的日志模式：非存放模式
   3.联机备份(热备份)，即一边处理业务操作，一边备份数据，这个时候，数据库必须是归档模式
       而默认的非归档模式，只能进行脱机备份














    




             
